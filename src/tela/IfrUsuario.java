    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        try {
            editavel = true;
            String codigoS = String.valueOf(tabelaPesquisarUsuario.getValueAt(tabelaPesquisarUsuario.getSelectedRow(), 0));
            String classe = String.valueOf(tabelaPesquisarUsuario.getValueAt(tabelaPesquisarUsuario.getSelectedRow(), 5));
            Usuario usuario = new UsuarioDAO().consultarId(Integer.parseInt(codigoS));
            if (usuario != null) {
                jTabbedPane1.setSelectedIndex(0);
                tfdNome.setText(usuario.getNome());
                tfdEndereco.setText(usuario.getEndereco());
                tfdEmail.setText(usuario.getEmail());
                tfdTelefone.setText(usuario.getTelefone());
                codigo = usuario.getCod();
                if (classe.equals("F")) {
                    jcbFuncionario.setSelected(true);
                    tfdUsuario.setEnabled(true);
                    pfdSenha.setEnabled(true);
                    tfdUsuario.setText(usuario.getUsuario());
                    pfdSenha.setText("");
                    tfdUsuario.requestFocus();

                } else {
                    tfdUsuario.setEnabled(false);
                    pfdSenha.setEnabled(false);
                    tfdUsuario.setText("");
                    jcbFuncionario.setSelected(false);
                }
            } else {
                JOptionPane.showMessageDialog(null, "Erro ao consultar registro!");
            }
        } catch (ArrayIndexOutOfBoundsException e) {
            JOptionPane.showMessageDialog(null, "Você precisa selecionar um registro para realizar a operação");
            System.out.println(e);
        }
    }//GEN-LAST:event_btnEditarActionPerformed

