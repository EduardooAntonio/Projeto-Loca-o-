    private void btnEditarSituacaoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarSituacaoActionPerformed
        try {
            String codigoS = String.valueOf(tabelaPesquisarUsuario.getValueAt(tabelaPesquisarUsuario.getSelectedRow(), 0));
            Usuario usuario = new UsuarioDAO().consultarId(Integer.parseInt(codigoS));
            char sit;
            if (usuario.getSituacao() == 'A') {
                sit = 'I';
            } else {
                sit = 'A';
            }
            String retorno = new UsuarioDAO().excluir(Integer.parseInt(codigoS), sit);
            if (retorno != null) {
                JOptionPane.showMessageDialog(null, "Situação alterada com sucesso!");
            }
            //new UsuarioDAO().popularTabela(tabelaPesquisarUsuario, tfdPesquisar.getText(), "" ); // revisar
        } catch (ArrayIndexOutOfBoundsException e) {
            JOptionPane.showMessageDialog(null, "Você precisa selecionar um registro para realizar a operação");
            System.out.println(e);
        }
    }//GEN-LAST:event_btnEditarSituacaoActionPerformed

