    private void btnSalvarCadastroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarCadastroActionPerformed
        boolean erro = false;
        try {
            Usuario usuario = new Usuario();
            usuario.setNome(tfdNome.getText());
            usuario.setEndereco(tfdEndereco.getText());
            usuario.setEmail(tfdEmail.getText());
            usuario.setTelefone(tfdTelefone.getText());
            System.out.println(codigo);
            usuario.setCod(codigo);
            usuario.setSituacao('A');
            usuario.setClasse('C');
            UsuarioDAO usuarioDAO = new UsuarioDAO();
            Usuario usuario1 = usuarioDAO.consultarId(codigo);
            String retorno = usuario1.getNome();

            if (jcbFuncionario.isSelected()) {
                usuario.setClasse('F');
                String user = tfdUsuario.getText();
                int tam_senha = pfdSenha.getText().length();
                String senha = CriptografiaAES.encrypt(pfdSenha.getText());
                usuario.setUsuario(user);
                usuario.setSenha(senha);
                if (Validacao.validaUsuario(usuario)) {
                    if (user.length() > 2) {
                        if (tam_senha > 4 || editavel) {

                            if (retorno == null) {
                                codigo = 0;

                            }
                            tfdUsuario.setText("");
                            pfdSenha.setText("");
                            tfdUsuario.requestFocus();

                        } else {
                            erro = true;
                            JOptionPane.showMessageDialog(null, "A senha é precisa ter pelo menos 5 caracteres");
                            pfdSenha.setText("");
                            pfdSenha.requestFocus();
                        }
                    } else {
                        erro = true;
                        JOptionPane.showMessageDialog(null, "Usuário muito curto. Inválido!");
                        tfdUsuario.setText("");
                        tfdUsuario.requestFocus();
                    }
                    if (!erro) {
                        if (codigo == 0) {
                            retorno = usuarioDAO.salvar(usuario);
                            JOptionPane.showMessageDialog(null, retorno);
                            tfdNome.setText("");
                            tfdEndereco.setText("");
                            tfdEmail.setText("");
                            tfdTelefone.setText("");
                            codigo = 0;
                        } else {
                            retorno = usuarioDAO.atualizar(usuario);
                            JOptionPane.showMessageDialog(null, retorno);
                            tfdNome.setText("");
                            tfdEndereco.setText("");
                            tfdEmail.setText("");
                            tfdTelefone.setText("");
                            codigo = 0;
                        }
                    }
                }
            } else {
                if (codigo == 0) {
                    retorno = usuarioDAO.salvar(usuario);
                    JOptionPane.showMessageDialog(null, retorno);
                    tfdNome.setText("");
                    tfdEndereco.setText("");
                    tfdEmail.setText("");
                    tfdTelefone.setText("");
                    codigo = 0;
                } else {
                    retorno = usuarioDAO.atualizar(usuario);
                    JOptionPane.showMessageDialog(null, retorno);
                    tfdNome.setText("");
                    tfdEndereco.setText("");
                    tfdEmail.setText("");
                    tfdTelefone.setText("");
                    codigo = 0;
                }
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro ao salvar cadastro!" + e);

        }


    }//GEN-LAST:event_btnSalvarCadastroActionPerformed

