    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        if (JOptionPane.showConfirmDialog(this, "Deseja realmente excluir?") == JOptionPane.OK_OPTION) {

            String idString = String.valueOf(tblTipoequipamento.getValueAt(tblTipoequipamento.getSelectedRow(), 0));

            int idExclusao = Integer.parseInt(idString);

            TipoequipamentoDAO teDAO = new TipoequipamentoDAO();
            String retorno = null;

            if (idExclusao > 0) {
                JOptionPane.showMessageDialog(null, "Registro excluído com sucesso!");
                retorno = teDAO.excluir(idExclusao);
                teDAO.popularTabela(tblTipoequipamento, tfdBusca.getText());
            } else {
                JOptionPane.showMessageDialog(null, "Problemas ao excluir registro.");
            }
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        try {
            String idString = String.valueOf(tblTipoequipamento.getValueAt(tblTipoequipamento.getSelectedRow(), 0));

            id = Integer.parseInt(idString);

            TipoequipamentoDAO teDAO = new TipoequipamentoDAO();

            Tipoequipamento tipoequipamento = teDAO.consultarId(id);

            if (tipoequipamento != null) {
                // define os valores do obj nos campos da tela
                tfdId.setText(String.valueOf(tipoequipamento.getId()));
                tfdDescricao.setText(tipoequipamento.getDescricao());

                // mudar aaba
                jTabbedPane1.setSelectedIndex(0);
                //definir foco
                tfdDescricao.requestFocus();
            }

        } catch (ArrayIndexOutOfBoundsException e) {
            JOptionPane.showMessageDialog(null, "Selecione um registro!");
            //e.printStackTrace();

        } catch (Exception e) {
            System.out.println(e);
        }
    }//GEN-LAST:event_btnEditarActionPerformed

