    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        Tipoequipamento tipoequipamento = new Tipoequipamento();
        //popular o obj
        tipoequipamento.setId(id);
        tipoequipamento.setDescricao(tfdDescricao.getText());

        //criar instancia Tipoequipamentodao
        TipoequipamentoDAO teDAO = new TipoequipamentoDAO();
        String retorno = null;
        // vou mandar salvar
        if ("".equals(tfdDescricao.getText())) {
            JOptionPane.showMessageDialog(null, "Preencha o campo descrição");
        } else if (tfdDescricao.getText() != null) {
            JOptionPane.showMessageDialog(null, "Registro salvo com sucesso!");

            // limpa campos
            tfdId.setText("");
            tfdDescricao.setText("");

            // posiciona cursor
            tfdDescricao.requestFocus();

            // resetar o id
            id = 0;
            retorno = teDAO.salvar(tipoequipamento);

        } else {
            JOptionPane.showMessageDialog(null, "Problemas ao salvar o registro!");
        }

    }//GEN-LAST:event_btnSalvarActionPerformed

