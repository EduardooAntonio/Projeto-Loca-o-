    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
            if (JOptionPane.showConfirmDialog(this, "Deseja realmente excluir?") == JOptionPane.OK_OPTION) {

            String idString = String.valueOf(tblEquipamento.getValueAt(tblEquipamento.getSelectedRow(), 0));

            int idExclusao = Integer.parseInt(idString);

            EquipamentoDAO eDAO = new EquipamentoDAO();
            String retorno = null;

            if (idExclusao > 0) {
                JOptionPane.showMessageDialog(null, "Registro excluído com sucesso!");
                retorno = eDAO.excluir(idExclusao);
                eDAO.popularTabela(tblEquipamento, tfdBusca.getText());
            } else {
                JOptionPane.showMessageDialog(null, "Problemas ao excluir registro.");
            }
        }    
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        String idString = String.valueOf(tblEquipamento.getValueAt(tblEquipamento.getSelectedRow(), 0));
        
        id = Integer.parseInt(idString);
        
        
        EquipamentoDAO eDAO = new EquipamentoDAO();
        
        Equipamento equipamento = eDAO.consultarId(id);
        System.out.println(equipamento.getId());
        System.out.println(equipamento.getDescricao());
        System.out.println(equipamento.getValor());
        System.out.println(equipamento.getTipoid());
        if (equipamento != null) {
            // define os valores do obj nos campos da tela
            tfdId.setText(String.valueOf(equipamento.getId()));
            tfdValor.setText(String.valueOf(equipamento.getValor()));
            tfdDescricao.setText(equipamento.getDescricao());
            // mudar aaba
            tabEquipamento.setSelectedIndex(0);
            //definir foco
            tfdDescricao.requestFocus();
            //cmbTipoEquipamento.setSelectedIndex(equipamento.getTipoid());
            ComboItem ci = new ComboItem();
            ci.setCodigo(equipamento.getTipoid());
            new CombosDAO().definirItemCombo(cmbTipoEquipamento, ci);
        }
    }//GEN-LAST:event_btnEditarActionPerformed

