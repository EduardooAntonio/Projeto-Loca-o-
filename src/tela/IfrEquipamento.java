    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        try {
        Equipamento equipamento = new Equipamento();
        Tipoequipamento tipoequipamento = new Tipoequipamento();
        //popular o obj
        equipamento.setId(id);
        equipamento.setDescricao(tfdDescricao.getText());
        equipamento.setValor(Integer.parseInt(tfdValor.getText()));
        ComboItem ci = (ComboItem) cmbTipoEquipamento.getSelectedItem();
        //System.out.println(ci.getCodigo());

        //criar instancia Tipoequipamentodao
        EquipamentoDAO eDAO = new EquipamentoDAO();
        String retorno = null; 
        // vou mandar salvar
        if("".equals(tfdDescricao.getText()) || "".equals(tfdValor.getText())) {
        JOptionPane.showMessageDialog(null, "Preencha o campo descrição");
        } else if (tfdDescricao.getText() != null) {
            int i= ci.getCodigo();
            equipamento.setTipoid(i);
            //System.out.println("tela=" + i);
            //*eDAO.salvar(equipamento);
            JOptionPane.showMessageDialog(null,eDAO.salvar(equipamento));
            
            // limpa campos
            tfdId.setText("");
            tfdDescricao.setText("");
            tfdValor.setText("");
            cmbTipoEquipamento.setSelectedItem(ABORT);
            

            // posiciona cursor
            tfdDescricao.requestFocus();

            // resetar o id
            id = 0;
            new EquipamentoDAO().popularTabela(tblEquipamento, tfdBusca.getText());
            
            
        } else {
            JOptionPane.showMessageDialog(null, "Problemas ao salvar o registro!");
        }
        
        
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Valor incorreto!");
                
        } catch (Exception e) {
            System.out.println(e);
        }
            
    }//GEN-LAST:event_btnSalvarActionPerformed

